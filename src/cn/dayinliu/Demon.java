package cn.dayinliu;

public class Demon {
    public static void main(String[] args) {
        int[] arr = {1};
        System.out.println(arr);  //数组地址

        char[] ch = {'a','b','c'};
        System.out.println(ch);  //a,b,c 方法中做了数组遍历

        byte[] bytes = {1};
        System.out.println(bytes);  //数组地址
    }
}
