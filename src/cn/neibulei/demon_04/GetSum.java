package cn.neibulei.demon_04;
/**
 * 类GetSum实现整数求和
 * @author yyb
 * @version 1.0
 */
public class GetSum {
    /**
     * 方法getSum功能计算两个整数的求和
     * 调用此方法传递两个int整数，返回求和值
     * @param a
     * @param b
     * @return sum
     */
    public int getSum(int a, int b){
        int sum;
        sum = a + b;
        return sum;
    }
}
